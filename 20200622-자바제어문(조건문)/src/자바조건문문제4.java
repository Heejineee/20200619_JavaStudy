/*
 * char 변수 선언 ==> 값을 대입
 * char alpha='A';
 * alpha가 대문자면 소문자 출력/소문자면 대문자 출력
 * 
 * 1. 제어문
 * 		연산자
 * 			= 비교연산자 : (== 같다 / != 같지않다 / < 작다 / > 크다 / 작거나 같다 <= / 크거나 같다 >=) ==> 최종 결과값 : true/false ==> 주로 조건문(if)에서 사용한다
 * 			= 논리연산자 : ( && 포함(기간, 범위) / || 미포함) 
 * 								 조건 || 조건 ==> 조건중에 한개 이상이 true일때 true
 * 			= 부정연산자 : 반대 ==> !
 * 		단독처리 => 산술연산자, 증감연산자, 대입연산자
 * 						  =======================
 * 								응용 => 제어문
 * 		연산처리 => 형변환 (강제 형변환)
 * 						   double => int ==> (int)double
 * 						   int => char ==> (char)int
 */
public class 자바조건문문제4 {

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		char alpha='a';
		if(alpha>='A' && alpha<='Z')
		{
			System.out.println((char)(alpha+32));
		}
		else
		{
			System.out.println((char)(alpha-32));
		}
	}

}
